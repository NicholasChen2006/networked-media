const express = require("express"); 
const multer = require("multer");   
const bodyParser = require("body-parser");
const nedb = require("@seald-io/nedb")

// what does this line do?
// A: it makes the url encoded parser body parser 
const urlEncodedParser = bodyParser.urlencoded({ extended: true }); 

// what is app?
// A: ????????????????????????????????????????????????
const app = express();

// what is this configuring?
// A: this makes multer upload the things into upload
const upload = multer({
  dest: "public/uploads",
});

// what is this configuring?
// A: this makes the database.txt with nedb
let database = new nedb({
  filename: "database.txt",
  autoload: true 
})

// what do each of these statements do?
// write the answer next to the line of code
app.use(express.static("public"));    // A: letting the program know we are using a folder called public for frontend files
app.use(urlEncodedParser);            // A: we are using urlEncodedParser to take data from the page????????????????????????????????????????????????
app.set("view engine", "ejs");        // A: setting the view engine as ejs 

// what type of request is this? what does it do?
// A: it makes the path for / which is the default path
app.get("/", (request, response) => {

  // how does this database search work?
  // A: if the query is empty it renders index.ejs with posts and data
  let query = {} 
  database.find(query).exec( (err, data)=>{
    response.render('index.ejs', {posts: data})
  } )
});

// what are the three parameters in this function?
// A: the path, the action, the function of the action????????????????????????????????????????????????????????????????????????????????????????????????
app.post("/upload", upload.single("theimage"), (req, res)=>{

  let currentDate = new Date()

  // what type of data structure is this?
  // A: OBJECT
  let data = {
    text: req.body.text,
    date: currentDate.toLocaleString(),
    timestamp: currentDate.getTime()
  }

  // why do we write this if statement?
  // A: to get the filename of the uploaded image to render it or smth????????????????????????????????????????????????
  if(req.file){
    data.image = "/uploads/" + req.file.filename
  }

  // what parameters go into this insert function?
  // A: ????????????????????????????????????????????????????????????????????????????????????????????????
  database.insert(data, (err, newData)=>{
    console.log(newData)
    res.redirect("/")
  })
})

// what does the number signify?
// A: the port to listen on to get into the website
// how do we access this on the web?
// A: (ip addy):6001/
app.listen(6001, () => {
  console.log("server started on port 6001");
});
